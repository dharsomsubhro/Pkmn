
public class pokemon {
	
	protected int speed;
	protected int HP;
	protected int attack;
	protected int defense;
	protected int sAttack;
	protected int sDefense;
	protected String name;
	
	public pokemon(int sped, int hp,int atk,int dfns, int sAttk,int sDef, String nam) {
		speed=sped;
		HP=hp;
		attack=atk;
		defense=dfns;
		sAttack=sAttk;
		sDefense=sDef;
		name=nam;	
	}
}
